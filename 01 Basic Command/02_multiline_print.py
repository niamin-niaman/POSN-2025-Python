"""
ตัวอย่างการพิมพ์ข้อความหลายบรรทัด
"""

# วิธีที่ 1: ใช้เครื่องหมาย """ (triple quotes)
print("""บรรทัดที่ 1
บรรทัดที่ 2
บรรทัดที่ 3""")

# วิธีที่ 2: ใช้ \n
print("บรรทัดที่ 1\nบรรทัดที่ 2\nบรรทัดที่ 3")

# วิธีที่ 3: ใช้ print แยกบรรทัด
print("บรรทัดที่ 1")
print("บรรทัดที่ 2")
print("บรรทัดที่ 3")

# ตัวอย่างการพิมพ์ข้อความหลายบรรทัดพร้อมการเยื้อง
print("""    บรรทัดที่ 1
    บรรทัดที่ 2
    บรรทัดที่ 3""") 