"""
ตัวอย่างการพิมพ์ข้อความภาษาไทย
"""
# พิมพ์ข้อความพื้นฐาน
print("Hello, World!")

# พิมพ์ข้อความภาษาไทยพื้นฐาน
print("สวัสดีครับ")
print("ยินดีที่ได้รู้จัก")

# พิมพ์ข้อความภาษาไทยพร้อมการขึ้นบรรทัดใหม่
print("สวัสดี\nครับ")

# พิมพ์ข้อความภาษาไทยพร้อมการเยื้อง
print("\tสวัสดีครับ")
print("\t\tยินดีที่ได้รู้จัก") 