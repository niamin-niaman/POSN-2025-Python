"""
ตัวอย่างการใช้ตัวอักษรพิเศษ
"""

# ตัวอักษรพิเศษพื้นฐาน
print("ขึ้นบรรทัดใหม่\nบรรทัดที่ 2")
print("เยื้อง 1 ครั้ง\tข้อความ")
print("เยื้อง 2 ครั้ง\t\tข้อความ")
print("ข้อความในเครื่องหมายคำพูด: \"Hello\"")
print("ข้อความในเครื่องหมายคำพูดเดี่ยว: \'World\'")

# ตัวอย่างการใช้งานร่วมกัน
print("ชื่อ:\tสมชาย")
print("อายุ:\t15")
print("ที่อยู่:\tกรุงเทพฯ\n\tประเทศไทย")

# ตัวอย่างการทำกรอบด้วยตัวอักษรพิเศษ
print("+----------------+")
print("|\tสวัสดี\t|")
print("+----------------+")

# ตัวอย่างการทำตาราง
print("ชื่อ\t\tอายุ\tคะแนน")
print("----------------------------")
print("สมชาย\t\t15\t85")
print("สมหญิง\t\t16\t90")

# ตัวอย่างการใช้ตัวอักษรพิเศษอื่นๆ
print("\\n คือตัวขึ้นบรรทัดใหม่")
print("\\t คือตัวเยื้อง")
print("\\\" คือเครื่องหมายคำพูด")
print("\\\' คือเครื่องหมายคำพูดเดี่ยว") 