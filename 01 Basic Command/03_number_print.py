"""
ตัวอย่างการพิมพ์ข้อความพร้อมตัวเลข
"""

# พิมพ์ข้อความพร้อมตัวเลขเต็ม
print("อายุ:", 15)
print("จำนวนนักเรียน:", 30)

# พิมพ์ข้อความพร้อมตัวเลขทศนิยม
print("คะแนน:", 85.5)
print("ราคา:", 99.99)

# พิมพ์ข้อความพร้อมการคำนวณ
print("5 + 3 =", 5 + 3)
print("10 x 2 =", 10 * 2)
print("100 ÷ 4 =", 100 / 4)

# พิมพ์ข้อความพร้อมตัวเลขหลายตัว
print("คะแนนสอบ:", 85, "คะแนน")
print("อายุ:", 15, "ปี", "ส่วนสูง:", 170, "cm") 