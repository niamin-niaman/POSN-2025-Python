"""
ตัวอย่างการพิมพ์ข้อความพร้อมตัวแปร
"""

# ประกาศตัวแปร
name = "สมชาย"
age = 15
height = 170.5
score = 85

# พิมพ์ข้อความพร้อมตัวแปร
print("ชื่อ:", name)
print("อายุ:", age)
print("ส่วนสูง:", height)
print("คะแนน:", score)

# พิมพ์ข้อความพร้อมตัวแปรหลายตัว
print("ชื่อ:", name, "อายุ:", age, "ปี")
print("ส่วนสูง:", height, "cm, คะแนน:", score, "คะแนน")

# พิมพ์ข้อความพร้อมการคำนวณกับตัวแปร
print("อายุปีหน้า:", age + 1)
print("คะแนนเพิ่ม 5:", score + 5)
print("ส่วนสูงเป็นเมตร:", height / 100) 