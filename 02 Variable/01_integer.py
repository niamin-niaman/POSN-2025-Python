"""
Integer Examples in Python
ตัวอย่างการใช้ตัวแปรประเภท Integer
"""

# ตัวอย่างการประกาศตัวแปร Integer
print("\n=== ตัวอย่างการประกาศตัวแปร Integer ===")
quantity = 100
year = 2024
temperature = -5

print("quantity =", quantity)
print("year =", year)
print("temperature =", temperature)

# ตัวอย่างการคำนวณพื้นฐาน
print("\n=== ตัวอย่างการคำนวณพื้นฐาน ===")
a = 10
b = 3

print("a + b =", a + b)  # บวก
print("a - b =", a - b)  # ลบ
print("a * b =", a * b)  # คูณ
print("a / b =", a / b)  # หาร
print("a // b =", a // b)  # หารปัดเศษลง
print("a % b =", a % b)  # เศษ
print("a ** b =", a ** b)  # ยกกำลัง

# ตัวอย่างการแปลงประเภทข้อมูล
print("\n=== ตัวอย่างการแปลงประเภทข้อมูล ===")
number = 42

print("แปลงเป็นข้อความ:", str(number))
print("แปลงเป็นทศนิยม:", float(number))
print("แปลงเป็นเลขฐาน 2:", bin(number))
print("แปลงเป็นเลขฐาน 16:", hex(number))
print("แปลงเป็นเลขฐาน 8:", oct(number)) 