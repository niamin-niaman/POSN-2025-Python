# Subtraction (-) Examples

# Basic subtraction with integers
num1 = 10
num2 = 5
result = num1 - num2
print(f"Basic subtraction: {num1} - {num2} = {result}")  # Output: 5

# Subtraction with decimals
decimal1 = 7.5
decimal2 = 2.5
result_decimal = decimal1 - decimal2
print(f"Decimal subtraction: {decimal1} - {decimal2} = {result_decimal}")  # Output: 5.0

# Subtraction with negative numbers
negative1 = -10
negative2 = -3
result_negative = negative1 - negative2
print(f"Negative subtraction: {negative1} - ({negative2}) = {result_negative}")  # Output: -7

# Order matters in subtraction
a = 5
b = 10
result1 = a - b
result2 = b - a
print(f"Order example 1: {a} - {b} = {result1}")  # Output: -5
print(f"Order example 2: {b} - {a} = {result2}")  # Output: 5 