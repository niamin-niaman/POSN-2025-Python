# Addition (+) Examples

# Basic addition with integers
num1 = 10
num2 = 5
result = num1 + num2
print(f"Basic addition: {num1} + {num2} = {result}")  # Output: 15

# Addition with decimals
decimal1 = 7.5
decimal2 = 2.5
result_decimal = decimal1 + decimal2
print(f"Decimal addition: {decimal1} + {decimal2} = {result_decimal}")  # Output: 10.0

# Addition with negative numbers
negative = -5
positive = 15
result_mixed = negative + positive
print(f"Mixed addition: {negative} + {positive} = {result_mixed}")  # Output: 10

# String concatenation with +
str1 = "Hello"
str2 = "World"
result_str = str1 + " " + str2
print(f"String concatenation: {str1} + ' ' + {str2} = {result_str}")  # Output: Hello World 