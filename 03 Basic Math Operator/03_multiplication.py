# Multiplication (*) Examples

# Basic multiplication with integers
num1 = 10
num2 = 5
result = num1 * num2
print(f"Basic multiplication: {num1} * {num2} = {result}")  # Output: 50

# Multiplication with decimals
decimal1 = 2.5
decimal2 = 3.0
result_decimal = decimal1 * decimal2
print(f"Decimal multiplication: {decimal1} * {decimal2} = {result_decimal}")  # Output: 7.5

# Multiplication with negative numbers
negative = -5
positive = 3
result_negative = negative * positive
print(f"Negative multiplication: {negative} * {positive} = {result_negative}")  # Output: -15

# Multiplication by zero
number = 42
result_zero = number * 0
print(f"Zero multiplication: {number} * 0 = {result_zero}")  # Output: 0

# String repetition with *
text = "Ha"
times = 3
result_str = text * times
print(f"String repetition: {text} * {times} = {result_str}")  # Output: HaHaHa 