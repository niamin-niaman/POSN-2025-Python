# Exponentiation (**) Examples

# Basic exponentiation
base = 2
power = 3
result = base ** power
print(f"Basic exponentiation: {base} ** {power} = {result}")  # Output: 8

# Exponentiation with negative power
num = 2
neg_power = -2
result_negative = num ** neg_power
print(f"Negative power: {num} ** {neg_power} = {result_negative}")  # Output: 0.25

# Zero power
number = 5
zero_power = 0
result_zero = number ** zero_power
print(f"Zero power: {number} ** {zero_power} = {result_zero}")  # Output: 1

# Decimal power
base = 4
decimal_power = 0.5
result_decimal = base ** decimal_power
print(f"Square root using decimal power: {base} ** {decimal_power} = {result_decimal}")  # Output: 2.0

# Large numbers
small_base = 2
large_power = 10
result_large = small_base ** large_power
print(f"Large number: {small_base} ** {large_power} = {result_large}")  # Output: 1024 