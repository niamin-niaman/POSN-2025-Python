# Basic Math Operators in Python

# Addition (+)
num1 = 10
num2 = 5
addition = num1 + num2
print(f"Addition: {num1} + {num2} = {addition}")  # Output: 15

# Subtraction (-)
subtraction = num1 - num2
print(f"Subtraction: {num1} - {num2} = {subtraction}")  # Output: 5

# Multiplication (*)
multiplication = num1 * num2
print(f"Multiplication: {num1} * {num2} = {multiplication}")  # Output: 50

# Division (/)
division = num1 / num2
print(f"Division: {num1} / {num2} = {division}")  # Output: 2.0

# Exponentiation (**)
exponentiation = num1 ** num2
print(f"Exponentiation: {num1} ** {num2} = {exponentiation}")  # Output: 100000

# Modulus (%)
modulus = num1 % num2
print(f"Modulus: {num1} % {num2} = {modulus}")  # Output: 0

# More examples with different numbers
print("\nMore Examples:")

# Example with decimals
decimal1 = 7.5
decimal2 = 2.5
print(f"Decimal addition: {decimal1} + {decimal2} = {decimal1 + decimal2}")  # Output: 10.0

# Example with negative numbers
negative1 = -10
negative2 = 3
print(f"Negative multiplication: {negative1} * {negative2} = {negative1 * negative2}")  # Output: -30

# Complex example combining operators
complex_calc = (num1 + num2) * 2 / (num2 ** 2)
print(f"Complex calculation: ({num1} + {num2}) * 2 / ({num2}^2) = {complex_calc}")  # Output: 1.2 